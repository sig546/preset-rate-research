import akshare as ak
import pandas as pd
import numpy as np

print("=" * 60)
print("预定利率研究值简化回溯分析（2025年）")
print("=" * 60)

# =====================
# 获取2025年10年期国债日频数据
# =====================
print("\n【获取2025年国债收益率数据】")
df = ak.bond_china_yield(start_date="20250101", end_date="20251231")
gz_df = df[df['曲线名称'] == '中债国债收益率曲线'].copy()
gz_df['日期'] = pd.to_datetime(gz_df['日期'])
gz_df = gz_df.sort_values('日期').reset_index(drop=True)
print(f"获取到 {len(gz_df)} 个交易日数据")

# 提取10年期数据
gz_df['10年收益率'] = gz_df['10年'].astype(float)

# =====================
# 获取2024年10年期国债数据（用于计算250MA）
# =====================
print("\n【获取2024年国债数据】")
df_2024 = ak.bond_china_yield(start_date="20240101", end_date="20241231")
gz_2024 = df_2024[df_2024['曲线名称'] == '中债国债收益率曲线'].copy()
gz_2024['日期'] = pd.to_datetime(gz_2024['日期'])
gz_2024 = gz_2024.sort_values('日期').reset_index(drop=True)
gz_2024['10年收益率'] = gz_2024['10年'].astype(float)
print(f"获取到 {len(gz_2024)} 个交易日数据")

# 合并数据
all_gz = pd.concat([gz_2024, gz_df], ignore_index=True)
all_gz = all_gz.sort_values('日期').reset_index(drop=True)

# =====================
# 计算各季度末的250MA
# =====================
print("\n【计算各季度末的250日移动平均】")

quarter_end_dates = [
    '2024-12-31',
    '2025-03-31',
    '2025-06-30',
    '2025-09-30',
    '2025-12-31',
]

for q_date in quarter_end_dates:
    q_dt = pd.to_datetime(q_date)
    # 找到该日期之前250个交易日的数据
    end_idx = all_gz[all_gz['日期'] <= q_dt].index[-1]
    start_idx = max(0, end_idx - 249)
    
    if end_idx >= start_idx:
        ma250 = all_gz.loc[start_idx:end_idx, '10年收益率'].mean()
        print(f"  {q_date}: 250MA = {ma250:.4f}% (使用{end_idx-start_idx+1}个交易日)")

# =====================
# 获取LPR和存款利率数据
# =====================
print("\n【LPR和存款利率数据】")

# 2025年LPR数据（从Excel中已知）
lpr_data = {
    '2025-01': 3.35, '2025-02': 3.35, '2025-03': 3.35,
    '2025-04': 3.10, '2025-05': 3.10, '2025-06': 3.10,
    '2025-07': 3.10, '2025-08': 3.10, '2025-09': 3.10,
    '2025-10': 3.10, '2025-11': 3.10, '2025-12': 3.10,
}

# 存款利率（从2024年10月起保持1.55%）
deposit_data = {
    '2025-01': 1.55, '2025-02': 1.55, '2025-03': 1.55,
    '2025-04': 1.55, '2025-05': 1.55, '2025-06': 1.55,
    '2025-07': 1.55, '2025-08': 1.55, '2025-09': 1.55,
    '2025-10': 1.55, '2025-11': 1.55, '2025-12': 1.55,
}

print("\n2025年各月LPR和存款利率:")
for month in lpr_data:
    print(f"  {month}: LPR={lpr_data[month]}%, 存款={deposit_data[month]}%, 合计={lpr_data[month]+deposit_data[month]}%")

# 计算各季度参考利率（前6个月平均/2）
print("\n【各季度参考利率计算】")
quarters = {
    '2024Q4': ['2024-07', '2024-08', '2024-09', '2024-10', '2024-11', '2024-12'],
    '2025Q1': ['2024-10', '2024-11', '2024-12', '2025-01', '2025-02', '2025-03'],
    '2025Q2': ['2024-11', '2024-12', '2025-01', '2025-02', '2025-03', '2025-04'],
    '2025Q3': ['2024-12', '2025-01', '2025-02', '2025-03', '2025-04', '2025-05'],
    '2025Q4': ['2025-01', '2025-02', '2025-03', '2025-04', '2025-05', '2025-06'],
}

# 2024年数据（已知）
_2024_lpr = {
    '2024-07': 3.85, '2024-08': 3.85, '2024-09': 3.85,
    '2024-10': 3.60, '2024-11': 3.60, '2024-12': 3.60,
}
_2024_deposit = {
    '2024-07': 1.80, '2024-08': 1.80, '2024-09': 1.80,
    '2024-10': 1.55, '2024-11': 1.55, '2024-12': 1.55,
}

for q_name, months in quarters.items():
    total = 0
    for m in months:
        if m in _2024_lpr:
            total += _2024_lpr[m] + _2024_deposit[m]
        else:
            total += lpr_data[m] + deposit_data[m]
    ref_rate = total / 6 / 2
    print(f"  {q_name}: 参考利率 = {ref_rate:.4f}%")

print("\n" + "=" * 60)
print("注意：以上仅为参考利率部分，完整计算需要国开债和进出口债数据")
print("=" * 60)
